-module(annlink_app).

-behaviour(application).

-include("annlink.hrl").

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    ?Info("---------------------------------------------------"),
    ?Info("STARTING ANNLINK"),
    Supervisor = annlink_sup:start_link(),
    ?Info("ANNLINK STARTED"),
    ?Info("---------------------------------------------------"),
    Supervisor.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

